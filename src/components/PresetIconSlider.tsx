function buildSvgString(path: string): string {
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" width="24" height="24" fill="none" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"><path d="${path}"/></svg>`
}

export function PresetIconSlider({ onIconSelected, selectedSvg }: PresetIconSliderProps) {
  const scrollRef = useRef<HTMLDivElement>(null)
  const [isAtEnd, setIsAtEnd] = useState(false)

  useEffect(() => {
    const el = scrollRef.current
    if (!el) return
    const check = () => setIsAtEnd(el.scrollLeft + el.clientWidth >= el.scrollWidth - 4)
    check()
    el.addEventListener('scroll', check, { passive: true })
    return () => el.removeEventListener('scroll', check)
  }, [])

  const handleClick = useCallback((icon: { name: string; path: string }) => {
    const svgString = buildSvgString(icon.path)
    onIconSelected({
      imageType: 'svg',
      imageDataUrl: null,
      svgString,
      imgWidth: 24,
      imgHeight: 24,
      bgColor: '',
    })
  }, [onIconSelected])

  return (
    <div className="mt-3">
      <p className="text-xs text-slate-500 font-medium mb-2.5">
        Or start with a free icon
        <span className="ml-1.5 text-slate-400 font-normal">MIT license · fully editable</span>
      </p>
      <div className="relative">
      <div
        ref={scrollRef}
        className="flex gap-2 overflow-x-auto pb-1"
        style={{ scrollbarWidth: 'none' }}
      >
        {PRESET_ICONS.map((icon) => {
          const svgString = buildSvgString(icon.path)
          const isSelected = selectedSvg === svgString
          return (
            <button
              key={icon.name}
              onClick={() => handleClick(icon)}
              aria-label={`Use ${icon.name} icon`}
              aria-pressed={isSelected}
              title={icon.name}
              className={`
                flex-none w-10 h-10 rounded-xl flex items-center justify-center
                border transition-all cursor-pointer
                ${isSelected
                  ? 'border-orange-400 bg-orange-50 text-orange-500 shadow-sm'
                  : 'border-slate-200 bg-white text-slate-500 hover:border-orange-300 hover:text-orange-400 hover:bg-orange-50/40'}
              `}
            >
              <svg
                viewBox="0 0 24 24"
                width="18"
                height="18"
                fill="none"
                stroke="currentColor"
                strokeWidth="1.5"
                strokeLinecap="round"
                strokeLinejoin="round"
                aria-hidden="true"
              >
                <path d={icon.path} />
              </svg>
            </button>
          )
        })}
      </div>
      {!isAtEnd && (
        <div className="pointer-events-none absolute right-0 top-0 bottom-1 w-10 bg-gradient-to-l from-white to-transparent flex items-center justify-end pr-1">
          <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2.5" strokeLinecap="round" strokeLinejoin="round" className="text-slate-400">
            <polyline points="9 18 15 12 9 6" />
          </svg>
        </div>
      )}
      </div>
    </div>
  )
}
